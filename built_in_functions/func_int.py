"""func_int.py将字符串或数字转换为整型"""

print(f'int() = {int()}')
print(f'int(3.14) = {int(3.14)}')
print(f'int("18") = {int("18")}')
print(f"int('111', base=2) = {int('111', base=2)}")
print(f"int('777', base=8) = {int('777', base=8)}")
print(f"int('FFF', base=16) = {int('FFF', base=16)}")
print(f"int(True) = {int(True)}")
print(f"int(False) = {int(False)}")
